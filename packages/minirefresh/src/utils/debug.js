/**
 * Warn
 * @param {String} msg
 */
export function warn(msg) {
    console.warn(`[Warn ] ${msg}`);
}

/**
 * Error
 * @param {String} msg
 */
export function error(msg) {
    console.error(`[Error ] ${msg}`);
}

/**
 * Log
 * @param {String} msg
 */
export function log(msg) {
    console.log(`[Log ] ${msg}`);
}
